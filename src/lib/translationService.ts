/**
 * Translation Service
 *
 * Calls the Supabase Edge Function `translate-text` which uses OpenAI
 * with a Smart Translation Router to translate board meeting / task content
 * into RU, UZ (Cyrillic), and EN.
 *
 * API key is never exposed to the frontend — it lives in Supabase secrets.
 *
 * To set the secret:
 *   supabase secrets set OPENAI_API_KEY=sk-...
 *
 * To deploy the function:
 *   supabase functions deploy translate-text --no-verify-jwt
 */

import { supabase, supabaseAnonKey } from "./supabaseClient";

export type SupportedLang = "ru" | "uz" | "en";

export type TranslationStatus =
  | "original"        // entered by user in source language
  | "auto_translated" // generated by OpenAI
  | "reviewed"        // auto-translated and manually checked/edited
  | "missing";        // not yet filled

export interface MeetingTranslationDraft {
  title_ru: string;
  title_uz: string;
  title_en: string;
  status_ru: TranslationStatus;
  status_uz: TranslationStatus;
  status_en: TranslationStatus;
  providerConnected: boolean;
}

