// ─── Profile translation ──────────────────────────────────────────────────────

export interface ProfileTranslationDraft {
  [key: string]: string;
}

/**
 * Translate multiple profile text fields from any source language to the other two.
 *
 * @param sourceLang — the language the user filled in ("ru", "en", or "uz")
 * @param fields — map of field keys (with suffix, e.g. "current_position_ru") to their values
 * @returns flat object with translations for the two target languages,
 *          e.g. { current_position_en: "...", current_position_uz: "..." }
 */
export async function generateProfileTranslations(
  sourceLang: SupportedLang,
  fields: Record<string, string>
): Promise<ProfileTranslationDraft> {
  const entries = Object.entries(fields).filter(([, v]) => v.trim());
  if (entries.length === 0) return {};

  // Combine all fields into one "description" for a single API call
  const DELIMITER = "\n===FIELD_SEPARATOR===\n";
  const combinedText = entries.map(([key, val]) => `[${key}]: ${val}`).join(DELIMITER);

  const { data: { session } } = await supabase.auth.getSession();
  if (!session) throw new Error("Not authenticated");

  const url = `${import.meta.env.VITE_SUPABASE_URL as string}/functions/v1/translate-text`;

  const res = await fetch(url, {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${session.access_token}`,
      "apikey": supabaseAnonKey,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      source_language: sourceLang,
      entity_type: "task",
      fields: { description: combinedText },
    }),
  });

  const result = await res.json();
  if (!res.ok) throw new Error(result.error || `Translation error (HTTP ${res.status})`);

  // Determine target languages (the two that are NOT the source)
  const targetLangs = (["ru", "en", "uz"] as const).filter((l) => l !== sourceLang);
  const draft: ProfileTranslationDraft = {};

  const suffixFromSource = sourceLang === "uz" ? "uz" : sourceLang;

  for (const targetLang of targetLangs) {
    const translatedText = result[targetLang]?.description || "";

    for (const entry of entries) {
      const key = entry[0]; // e.g. "current_position_ru"
      // Strip source suffix to get base name
      const base = key.replace(new RegExp(`_${suffixFromSource}$`), "");
      const regex = new RegExp(`\\[${key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\]:\\s*(.+?)(?=\\[|===FIELD_SEPARATOR===|$)`, "s");
      const match = translatedText.match(regex);
      if (match) {
        draft[`${base}_${targetLang}`] = match[1].trim();
      }
    }
  }

  return draft;
}

// ─── Notification translation ─────────────────────────────────────────────────

/**
 * Batch-translate notification titles and bodies to a target language.
 * Groups them into one API call using a delimiter trick.
 * Returns a map of notificationId → { title, body }.
 */
export async function translateNotificationTexts(
  items: { id: string; title: string; body: string }[],
  targetLang: "uz" | "en"
): Promise<Record<string, { title: string; body: string }>> {
  if (items.length === 0) return {};

  const { data: { session } } = await supabase.auth.getSession();
  if (!session) return {};

  // Use short indexed markers ([T0], [B0]) instead of UUIDs —
  // AI reliably preserves short alphanumeric tags during translation.
  const combined = items
    .map((n, i) => `[T${i}]: ${n.title}\n[B${i}]: ${n.body}`)
    .join("\n");

  const url = `${import.meta.env.VITE_SUPABASE_URL as string}/functions/v1/translate-text`;

  try {
    const res = await fetch(url, {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${session.access_token}`,
        "apikey": supabaseAnonKey,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        source_language: "ru",
        entity_type: "task",
        fields: { description: combined },
      }),
    });

    if (!res.ok) return {};
    const result = await res.json();
    const translatedText: string = result[targetLang]?.description || "";

    const out: Record<string, { title: string; body: string }> = {};
    for (let i = 0; i < items.length; i++) {
      // Stop capture at next [Tx] or [Bx] marker, or end of string
      const titleMatch = translatedText.match(
        new RegExp(`\\[T${i}\\]:\\s*(.+?)(?=\\n\\[|$)`, "s")
      );
      const bodyMatch = translatedText.match(
        new RegExp(`\\[B${i}\\]:\\s*(.+?)(?=\\n\\[|$)`, "s")
      );
      out[items[i].id] = {
        title: titleMatch?.[1]?.trim() || items[i].title,
        body:  bodyMatch?.[1]?.trim()  || items[i].body,
      };
    }
    return out;
  } catch {
    return {};
  }
}

// ─── UI helpers ───────────────────────────────────────────────────────────────

/** Human-readable dot/symbol for a translation status */
export function translationStatusLabel(status: TranslationStatus): string {
  switch (status) {
    case "original":        return "●";
    case "auto_translated": return "✦";
    case "reviewed":        return "✓";
    case "missing":         return "○";
  }
}

/** Colour for a translation status indicator */
export function translationStatusColor(status: TranslationStatus): string {
  switch (status) {
    case "original":        return "#059669"; // green
    case "auto_translated": return "#7C3AED"; // purple
    case "reviewed":        return "#2563EB"; // blue
    case "missing":         return "#D1D5DB"; // gray
  }
}
